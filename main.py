def greaterThan(x, y):
    if x > y:
        return True
    else:
        return False

def lessThan(x, y):
    if x < y:
        return True
    else:
        return False

def equalTo(x, y):
    if x == y:
        return True
    else:
        return False

def greaterOrEqual(x, y):
    if x > y or x == y:
        return True
    else:
        return False

def lessOrEqual(x, y):
    if x < y or x == y:
        return True
    else:
        return False